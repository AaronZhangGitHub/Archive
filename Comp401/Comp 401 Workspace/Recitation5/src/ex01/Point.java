package ex01;

public class Point {
	// Point Class Encapsulates Two Coordinates
	private int x;
	private int y;

	// Constructor
	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	// X Getter
	public int getX() {
		return x;
	}
	
	// Y Getter
	public int getY() {
		return y;
	}
}