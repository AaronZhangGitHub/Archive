package ex01;

public class DPoint {
	// Point Class Encapsulates Two Coordinates
	private double x;
	private double y;

	// Constructor
	public DPoint(double x, double y) {
		this.x = x;
		this.y = y;
	}
	
	// X Getter
	public double getX() {
		return x;
	}
	
	// Y Getter
	public double getY() {
		return y;
	}
}